    private function withIanaTimezones(array $data): array
    {
        $iso = strtoupper((string) ($data['iso_alpha2'] ?? ''));
        $timezones = timezone_identifiers_list(\DateTimeZone::PER_COUNTRY, $iso) ?: [];
        $preferred = $this->preferredDefaultTimezone($iso);

        if ($preferred && in_array($preferred, $timezones, true)) {
            $data['timezone'] = $preferred;
        } elseif (in_array($data['timezone'] ?? '', timezone_identifiers_list(), true)) {
            $data['timezone'] = $data['timezone'];
        } else {
            $data['timezone'] = $timezones[0] ?? 'UTC';
        }

        $settings = is_array($data['settings'] ?? null) ? $data['settings'] : [];
        $settings['timezones'] = array_values(array_unique($timezones ?: [$data['timezone']]));
        $settings['tax_calendar_defaults'] = $this->mergeComplianceDefaults(
            is_array($settings['tax_calendar_defaults'] ?? null) ? $settings['tax_calendar_defaults'] : [],
            $iso === 'TZ' ? $this->tanzaniaComplianceDefaults() : $this->baseComplianceDefaults()
        );
        $data['settings'] = $settings;

        return $data;
    }

    private function mergeComplianceDefaults(array $existing, array $defaults): array
    {
        $existingKeys = collect($existing)
            ->pluck('key')
            ->filter()
            ->all();

        $missing = collect($defaults)
            ->reject(fn ($default) => in_array($default['key'] ?? null, $existingKeys, true))
            ->values()
            ->all();

        return array_values(array_merge($existing, $missing));
    }

